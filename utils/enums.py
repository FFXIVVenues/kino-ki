from __future__ import annotations

from enum       import Enum
######################################################################
__all__ = (
    "Player",
)
######################################################################
class Player(Enum):

    P1 = 1
    P2 = 2

######################################################################
    def switch(self) -> Player:

        if self.value == 1:
            return Player.P2
        else:
            return Player.P1

